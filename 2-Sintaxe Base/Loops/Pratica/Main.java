package Loops.Pratica;

public class Main {
    public static void main(String[] args) {

        //For :

        System.out.println("For :");
        for (int n = 10 ; n > 0 ; n--){

            System.out.println(n);

        }

        int i = 10;


        //While :
        System.out.println("While : ");
        while (i  >= 0){
            System.out.println(i);
            i--;
        }



    }
}
