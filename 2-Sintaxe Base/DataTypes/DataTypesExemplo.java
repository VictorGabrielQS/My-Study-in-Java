package DataTypes;

public class DataTypesExemplo {
    public static void main(String[] args) {

        int idade = 20;
        double altura = 1.75;
        char inicial = 'V';
        boolean ativo = true;

        System.out.println("Idade : " + idade);
        System.out.println("Altura : " + altura);
        System.out.println("Inicial : " + inicial);
        System.out.println("Ativo : " + ativo);


    }
}
