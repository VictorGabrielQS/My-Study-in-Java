package Loops;

public class LoopDoWhile {
    public static void main(String[] args) {


/*
        do{
             // O bloco de código é executado pelo menos uma vez, mesmo que a condição seja falsa.

        }while (condição);

*/



        int i = 6;

        do {

            System.out.println("Interação : " + i);
            i++;

        }while (i <= 5 );

    }
}
