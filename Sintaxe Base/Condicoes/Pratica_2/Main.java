/*

Desafio 1: Verificação de Número Positivo ou Negativo

Escreva um programa que receba um número inteiro
e verifique se ele é positivo, negativo ou zero.

*/

public class Main {

    public static void main(String[] args) {

        int numero;

        numero = 1;

        if(numero >= 1) {
            System.out.println( numero +" e Positivo");

        }
        else {
            System.out.println("Seu numero e negativo");

        }




    }

}
