package Loops;


public class LoopWhile {
    public static void main(String[] args) {




        /*

        while( condição ){

        while é usado quando você quer repetir um bloco de código
        enquanto uma condição for verdadeira

        }

        */



        //While :
        int i = 1;

        while (i <= 5){

            System.out.println("Iteração : " + i);

            i++;
        }




    }
}
