package Condições;

public class CondicionalIf {
    public static void main(String[] args) {

        int nota = 75;

        if ( nota >= 90 ){
            System.out.println("A");

        }else if ( nota >= 75 ){
            System.out.println("B");

        }else {
            System.out.println("C");

        }
    }
}
